#include <stdio.h>

int main() {

    // Khai báo 3 biến: int, float, char. Gán giá trị cho mỗi biến và in ra màn hình.
    // Init Variables
    int a = 100;
    float b = 3.9;
    char c = 'C';

    // Display
    printf("Integer: %d\n", a);
    printf("Floating Point Number: %.2f\n", b);
    printf("Character: %c\n", c);

    return 0;
}